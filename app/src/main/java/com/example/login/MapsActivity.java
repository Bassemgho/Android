public class MapsActivity extends FragmentActivity implements OnMapReadyCallback {
    LocationManager locationManager;
    LocationListener locationListener;
    LatLng userLatlong;

    private GoogleMap mMap;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_maps);
        // Obtain the SupportMapFragment and get notified when the map is ready to be used.
        SupportMapFragment mapFragment = (SupportMapFragment) getSupportFragmentManager()
                .findFragmentById(R.id.map);
        mapFragment.getMapAsync(this);
    }
    @Override
    public void onMapReady(GoogleMap googleMap) {
        mMap = googleMap;
        LatLng twincenter= new LatLng(33,-7);
        mMap.addMarker(new MarkerOptions().position(twincenter).title("twincenter"));

        mMap.moveCamera(CameraUpdateFactory.newLatLng(twincenter));
        mMap.moveCamera(CameraUpdateFactory.newLatLngZoom(twincenter,10));

        mMap.getUiSettings().setZoomControlsEnabled(true);

        CircleOptions circlesOptions = new CircleOptions();
        circlesOptions.center(twincenter);
        circlesOptions.radius(700);
        circlesOptions.fillColor(Color.TRANSPARENT);
        circlesOptions.strokeWidth(6);
        mMap.addCircle(circlesOptions);



    }
}
